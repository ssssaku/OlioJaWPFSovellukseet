namespace Harjoitus18KT
{
    public static class Laskuri
    {
        public static double summa(double a, double b)
        {
            return a + b;
        }

        public static double erotus(double a, double b)
        {
            return a - b;
        }

        public static double kerto(double a, double b)
        {
            return a * b;
        }

        public static double jako(double a, double b)
        {
            return a / b;
        }
    }
}